public class ForBasicPractice {
    public static void main(String[] args) {
        for(int i=21; i<30; i++) {
            if(i % 3 == 0) {
                System.out.println(i+"은 3의 배수입니다.");
            }
            if(i % 4 == 0) {
                System.out.println(i+"은 4의 배수입니다.");
            }
            if(i % 5 == 0) {
                System.out.println(i+"은 5의 배수입니다.");
            }
            if(i % 6 == 0) {
                System.out.println(i+"은 6의 배수입니다.");
            }
            if(i % 7 == 0) {
                System.out.println(i+"은 7의 배수입니다.");
            }
            if(i % 9 == 0) {
                System.out.println(i+"은 9의 배수입니다.");
            }
        }
    }
}
